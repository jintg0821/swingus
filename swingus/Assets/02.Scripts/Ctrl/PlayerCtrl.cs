using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class PlayerCtrl : MonoBehaviour
{
    public float speed = 10.0f;
    public float rotateSpeed = 10.0f;
    public float jump = 10.0f;
    public float boostDuration = 3.0f;
    public float boostMultiplier = 2.0f;
    bool isJumping = false;
    bool isBoosted = false;
    private float originalSpeed;

    float h, v;

    public AudioSource poopSfx;
    public AudioSource itemSfx;
    public AudioSource napzakSfx;

    private Rigidbody rb;
    private new AudioSource audio;

    void Start()
    {
        audio = GetComponent<AudioSource>();
        rb = GetComponent<Rigidbody>();
        originalSpeed = speed;
    }

    void FixedUpdate()
    {
        h = Input.GetAxis("Horizontal");
        v = Input.GetAxis("Vertical");

        Vector3 dir = new Vector3(h, 0, v); // new Vector3(h, 0, v)가 자주 쓰이게 되었으므로 dir이라는 변수에 넣고 향후 편하게 사용할 수 있게 함

        // 바라보는 방향으로 회전 후 다시 정면을 바라보는 현상을 막기 위해 설정
        if (!(h == 0 && v == 0))
        {
            // 이동과 회전을 함께 처리
            transform.position += dir * GetPlayerSpeed() * Time.deltaTime;
            // 회전하는 부분. Point 1.
            transform.rotation = Quaternion.Lerp(transform.rotation, Quaternion.LookRotation(dir), Time.deltaTime * rotateSpeed);
        }
    }

    private float GetPlayerSpeed()
    {
        if (isBoosted)
        {
            // 일시적으로 속도를 증가시킴
            return speed * boostMultiplier;
        }
        else
        {
            return speed;
        }
    }

    public void OnTriggerEnter(Collider other)
    {
        if (other.tag == "Poop")
        {
            poopSfx.Play();
        }

        if (other.tag == "Item")
        {
            itemSfx.Play();   
        }

        if (other.tag == "Napzak")
        {
            napzakSfx.Play();
            BoostPlayerSpeed();
        }
    }

    private void BoostPlayerSpeed()
    {
        isBoosted = true;

        // 속도를 원래 값으로 복원하기 위해 일정 시간 후에 isBoosted를 false로 변경
        Invoke("ResetPlayerSpeed", boostDuration);
    }

    private void ResetPlayerSpeed()
    {
        isBoosted = false;
    }

    void OnCollisionEnter(Collision col)
    {
        if (col.transform.name == "Ground")
        {
            isJumping = false;
        }
    }
}